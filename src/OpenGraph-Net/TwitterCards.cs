namespace OpenGraph_Net
{
    /// <summary>
    /// Twitter cards metas  "twitter:...."
    /// </summary>
    public class TwitterCards
    {
        /// <summary>
        /// twitter:creator
        /// </summary>
        public string Creator { get; set; }

        /// <summary>
        /// twitter:image
        /// </summary>
        public string Image { get; set; }

        /// <summary>
        /// twitter:site
        /// </summary>
        public string Site { get; set; }

        /// <summary>
        /// twitter:title
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// twitter:description
        /// </summary>
        public string Description { get; set; }

        /// <summary>
        /// twitter:card
        /// </summary>
        public string Card { get; set; }

        /// <summary>
        /// twitter:player
        /// </summary>
        public string Player { get; set; }
    }
}
